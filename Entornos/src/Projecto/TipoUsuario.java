package Projecto;

/**
 * El enum TipoUsuario define los tipos de usuario disponibles.
 */
public enum TipoUsuario {
	/**
	 * El Normal hace que se instancie el usuario como tipo normal.
	 */
    NORMAL,
    /**
	 * El Normal hace que se instancie el usuario como tipo normal, y el administrador lo mismo pero con admin.
	 */
    ADMINISTRADOR
    
}
